/*

Vamos a ver los componentes controlados y los no controlados:

Los componentes controlados:
Son aquellos componentes que pueden mantener su propio estado y ser actualizados con base en la informacion suministrada por el usuario. -> input, select, radio button. etc.

Los componentes no controlados:
Son aquellos componentes en los cuales a nivel de programacion no los podemos controlar, simplemente ellos gestionan su propio estado, hacen lo que tienen que hacer y solamente proveen una respuesta. -> un slider.

*/