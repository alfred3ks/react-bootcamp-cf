/*

Los Hooks: Lo que hemos visto de useStare eso es un hook.

Los hooks son como funciones que normalmente comienzan con la palabra 'use'.

Nos permiten reutilizar codigo. Nosotros mismos podemos crear hooks personalizados. Entre algunas hooks mas usado tenemos:
- useState(),
- useEfect().

Los hooks siguen unas reglas:
- No pueden estar dentro de un consicional,
- Se tienen que llamar en el top-level del componente,
Solo se pueden invocar en function components (o en otros hooks).

En la documentacion de react podemos ver sobre las reglas de los hooks:

https://es.reactjs.org/docs/hooks-rules.html

*/